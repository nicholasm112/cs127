import turtle

nick = turtle.Turtle()
nm = turtle.Screen()
nm.bgcolor("khaki")

nick.color("black")
nick.pensize(3)
nick.shape("turtle")

for i in range(6):
    nick.fillcolor("green")
    nick.forward(100)
    nick.left(45)
    nick.forward(100)
    nick.left(135)
    nick.stamp()
    nick.forward(100)
    nick.left(45)
    nick.forward(100)
    nick.right(165)
   


